DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'V2-LEADERSHIP-PROGRAMME-GROWTH-LOG'
      AND policyname = 'submissions_read_all'
  ) THEN
    CREATE POLICY "submissions_read_all"
      ON public."V2-LEADERSHIP-PROGRAMME-GROWTH-LOG"
      FOR SELECT USING (true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'V2-LEADERSHIP-PROGRAMME-GROWTH-LOG'
      AND policyname = 'submissions_insert_all'
  ) THEN
    CREATE POLICY "submissions_insert_all"
      ON public."V2-LEADERSHIP-PROGRAMME-GROWTH-LOG"
      FOR INSERT WITH CHECK (true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'V2-LEADERSHIP-PROGRAMME-GROWTH-LOG'
      AND policyname = 'submissions_update_all'
  ) THEN
    CREATE POLICY "submissions_update_all"
      ON public."V2-LEADERSHIP-PROGRAMME-GROWTH-LOG"
      FOR UPDATE USING (true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'V2-LEADERSHIP-PROGRAMME-GROWTH-LOG'
      AND policyname = 'submissions_delete_all'
  ) THEN
    CREATE POLICY "submissions_delete_all"
      ON public."V2-LEADERSHIP-PROGRAMME-GROWTH-LOG"
      FOR DELETE USING (true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'form_config'
      AND policyname = 'config_read_all'
  ) THEN
    CREATE POLICY "config_read_all"
      ON public.form_config
      FOR SELECT USING (true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'form_config'
      AND policyname = 'config_write_auth'
  ) THEN
    CREATE POLICY "config_write_auth"
      ON public.form_config
      FOR ALL USING (auth.role() = 'authenticated');
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'teachers'
      AND policyname = 'teachers_read_all'
  ) THEN
    CREATE POLICY "teachers_read_all"
      ON public.teachers
      FOR SELECT USING (true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'teachers'
      AND policyname = 'teachers_write_auth'
  ) THEN
    CREATE POLICY "teachers_write_auth"
      ON public.teachers
      FOR ALL USING (auth.role() = 'authenticated');
  END IF;
END $$;

