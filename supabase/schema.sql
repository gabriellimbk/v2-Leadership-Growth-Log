-- Run this in Supabase Dashboard -> SQL Editor.
-- This configures the existing public."V2-LEADERSHIP-PROGRAMME-GROWTH-LOG"
-- table used by the app. It does not drop the table.

CREATE TABLE IF NOT EXISTS public."V2-LEADERSHIP-PROGRAMME-GROWTH-LOG" (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Student submissions: one row per student, keyed by Firebase UID.
ALTER TABLE public."V2-LEADERSHIP-PROGRAMME-GROWTH-LOG"
  ADD COLUMN IF NOT EXISTS student_uid TEXT,
  ADD COLUMN IF NOT EXISTS student_email TEXT,
  ADD COLUMN IF NOT EXISTS student_name TEXT NOT NULL DEFAULT '',
  ADD COLUMN IF NOT EXISTS teacher_id TEXT NOT NULL DEFAULT 'Teacher A',
  ADD COLUMN IF NOT EXISTS answers JSONB NOT NULL DEFAULT '{}',
  ADD COLUMN IF NOT EXISTS comments JSONB NOT NULL DEFAULT '{}',
  ADD COLUMN IF NOT EXISTS status TEXT NOT NULL DEFAULT 'draft',
  ADD COLUMN IF NOT EXISTS updated_at TIMESTAMPTZ DEFAULT NOW();

ALTER TABLE public."V2-LEADERSHIP-PROGRAMME-GROWTH-LOG"
  DROP CONSTRAINT IF EXISTS v2_leadership_growth_log_status_check,
  ADD CONSTRAINT v2_leadership_growth_log_status_check
    CHECK (status IN ('draft', 'submitted', 'reviewed'));

CREATE UNIQUE INDEX IF NOT EXISTS v2_leadership_growth_log_student_uid_idx
  ON public."V2-LEADERSHIP-PROGRAMME-GROWTH-LOG"(student_uid);

-- Form configuration: one row, editable by teachers.
CREATE TABLE IF NOT EXISTS public.form_config (
  id TEXT PRIMARY KEY DEFAULT 'default',
  config JSONB NOT NULL,
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Teacher directory: editable by admins from the Admin Console.
CREATE TABLE IF NOT EXISTS public.teachers (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name TEXT NOT NULL,
  email TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE UNIQUE INDEX IF NOT EXISTS teachers_email_idx
  ON public.teachers (LOWER(email));

